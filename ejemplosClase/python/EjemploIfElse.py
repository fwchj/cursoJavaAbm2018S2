#Ejemplo if-else in Python

#Ejemplo binario hombre/mujer
mujer = 0
if mujer==1:
    print("Es mujer")
else:
    print("Es hombre")


#Lo mismo con un boolean
mujer = True
if mujer:
    print("Es mujer")
else:
    print("Es hombre")

#Ejemplo de intervalos => ingreso
ingreso = 45000;
if ingreso<5000:
    print("Clase baja")
elif ingreso<20000:
    print("Clase media")
else:
    print("Clase alta")
    

